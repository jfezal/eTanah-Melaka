package com.theta.kutipanoffline.model.entities;

import java.math.BigDecimal;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

import lombok.Data;

@Data
@Entity
@Table(name = "akaun")
public class Akaun {
	@Id
	@Column(name = "no_akaun")
	String noAkaun;
	
	@Column(name = "id_hakmilik")
	String idHakmilik;
	
	@Column(name = "amaun")
	BigDecimal amaun;
	
	@Column(name = "kod_daerah")
	String kodDaerah;
	
	@Column(name = "kod_caw")
	String kodCawangan;
	
	@Column(name="kod_bpm")
	String kodBpm;
	
	@Column(name="nama")
	String namaPemegang;
	
	@Column(name="alamat1")
	String alamat1;
	
	@Column(name="alamat2")
	String alamat2;
	
	@Column(name="alamat3")
	String alamat3;
	
	@Column(name="alamat4")
	String alamat4;
	
	@Column(name="poskod")
	Integer poskod;
	
	@Column(name="negeri")
	String negeri;

}
