package etanah.model.view;

import java.io.Serializable;
import java.math.BigDecimal;
import javax.persistence.Column;
import javax.persistence.Embeddable;

/**
 *
 * @author mohd.faidzal
 */
@Embeddable
public class PPPKCekBankDraft implements Serializable {
     @Column (name = "caw")
    private String caw;
      @Column (name = "kod_trans")
    private String kodTrans;
       @Column (name = "no_penyata")
    private String noPenyata;
       @Column (name = "cb_amaun")
    private BigDecimal cbAmaun;

       @Column (name = "cb_no_ruj")
    private String cbNoRuj;
       @Column (name = "cb_bank_swift")
    private String cbBankSwift;

    public String getCaw() {
        return caw;
    }

    public void setCaw(String caw) {
        this.caw = caw;
    }

    public String getKodTrans() {
        return kodTrans;
    }

    public void setKodTrans(String kodTrans) {
        this.kodTrans = kodTrans;
    }

    public String getNoPenyata() {
        return noPenyata;
    }

    public void setNoPenyata(String noPenyata) {
        this.noPenyata = noPenyata;
    }

    public BigDecimal getCbAmaun() {
        return cbAmaun;
    }

    public void setCbAmaun(BigDecimal cbAmaun) {
        this.cbAmaun = cbAmaun;
    }
    public String getCbNoRuj() {
        return cbNoRuj;
    }

    public void setCbNoRuj(String cbNoRuj) {
        this.cbNoRuj = cbNoRuj;
    }

    public String getCbBankSwift() {
        return cbBankSwift;
    }

    public void setCbBankSwift(String cbBankSwift) {
        this.cbBankSwift = cbBankSwift;
    }
       
       
}
