public class StatusTukarGantiForm {
    String daerah;
    String diDaftarOleh;
    String idHakmilik;
    String noLot;
    String tarikh;
    String versi;

    public String getDaerah() {
        return daerah;
    }

    public void setDaerah(String daerah) {
        this.daerah = daerah;
    }

    public String getDiDaftarOleh() {
        return diDaftarOleh;
    }

    public void setDiDaftarOleh(String diDaftarOleh) {
        this.diDaftarOleh = diDaftarOleh;
    }

    public String getIdHakmilik() {
        return idHakmilik;
    }

    public void setIdHakmilik(String idHakmilik) {
        this.idHakmilik = idHakmilik;
    }

    public String getNoLot() {
        return noLot;
    }

    public void setNoLot(String noLot) {
        this.noLot = noLot;
    }

    public String getTarikh() {
        return tarikh;
    }

    public void setTarikh(String tarikh) {
        this.tarikh = tarikh;
    }

    public String getVersi() {
        return versi;
    }

    public void setVersi(String versi) {
        this.versi = versi;
    }
    
    
}
