    public void setStatusSftp(String statusSftp) {
        this.statusSftp = statusSftp;
    }

    public String getGPGFileName() {
        return GPGFileName;
    }

    public void setGPGFileName(String GPGFileName) {
        this.GPGFileName = GPGFileName;
    }

    public String getIddFileName() {
        return iddFileName;
    }

    public void setIddFileName(String iddFileName) {
        this.iddFileName = iddFileName;
    }
    
