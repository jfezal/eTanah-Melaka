package etanah.service.ispeks;

public class UploadFileForm implements java.io.Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -712650772706788811L;
	
	private String localFileLocation;
	private String sftpFileLocation;
	private String fileName;
	private String uploadAt;
	
	public String getLocalFileLocation() {
		return localFileLocation;
	}
	public void setLocalFileLocation(String localFileLocation) {
		this.localFileLocation = localFileLocation;
	}
	public String getSftpFileLocation() {
		return sftpFileLocation;
	}
	public void setSftpFileLocation(String sftpFileLocation) {
		this.sftpFileLocation = sftpFileLocation;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String localFileName) {
		this.fileName = localFileName;
	}
	public String getUploadAt() {
		return uploadAt;
	}
	public void setUploadAt(String uploadAt) {
		this.uploadAt = uploadAt;
	}
	
	
}
