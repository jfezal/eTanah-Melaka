package etanah.view.stripes.penguatkuasaan.disClass;

import etanah.view.stripes.pelupusan.disClass.*;

/**
 *
 * @author afham
 */
public class DisBorangController {
    
    private boolean tBorang = Boolean.TRUE ;
    
     public DisBorangController editBorang() {
         DisBorangController disBControl = new DisBorangController();
         disBControl.settBorang(Boolean.TRUE);
 
         return disBControl ;      
    }
     
     public DisBorangController viewBorang() {
         DisBorangController disBControl = new DisBorangController();
         disBControl.settBorang(Boolean.FALSE);
 
         return disBControl ;      
    }

    public boolean istBorang() {
        return tBorang;
    }

    public void settBorang(boolean tBorang) {
        this.tBorang = tBorang;
    }
    
     
}
