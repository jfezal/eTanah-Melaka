package etanah.view.stripes.penguatkuasaan.disClass;

import etanah.view.stripes.pelupusan.disClass.*;

/**
 *
 * @author afham
 */
public class DisLaporanPelanController {
    
    private boolean sTanah = Boolean.TRUE ;
    private boolean pTerdahulu = Boolean.TRUE ;
    private boolean dKawasan = Boolean.TRUE ;
    private boolean catatan = Boolean.TRUE ;
    private boolean charting = Boolean.TRUE ;
    
    public DisLaporanPelanController viewOnlyLaporanPelanPT() {
         DisLaporanPelanController disLPControl = new DisLaporanPelanController();
         disLPControl.setsTanah(Boolean.TRUE);
         disLPControl.setpTerdahulu(Boolean.TRUE);
         disLPControl.setdKawasan(Boolean.TRUE);
         disLPControl.setCatatan(Boolean.TRUE);
         disLPControl.setCharting(Boolean.TRUE);
         
         return disLPControl ;      
    }
    
    public DisLaporanPelanController viewOnlyLaporanPelan() {
         DisLaporanPelanController disLPControl = new DisLaporanPelanController();
         disLPControl.setsTanah(Boolean.FALSE);
         disLPControl.setpTerdahulu(Boolean.FALSE);
         disLPControl.setdKawasan(Boolean.FALSE);
         disLPControl.setCatatan(Boolean.FALSE);
         disLPControl.setCharting(Boolean.FALSE);
         
         return disLPControl ;      
    }

    public boolean issTanah() {
        return sTanah;
    }

    public void setsTanah(boolean sTanah) {
        this.sTanah = sTanah;
    }

    public boolean ispTerdahulu() {
        return pTerdahulu;
    }

    public void setpTerdahulu(boolean pTerdahulu) {
        this.pTerdahulu = pTerdahulu;
    }

    public boolean isdKawasan() {
        return dKawasan;
    }

    public void setdKawasan(boolean dKawasan) {
        this.dKawasan = dKawasan;
    }

    public boolean isCatatan() {
        return catatan;
    }

    public void setCatatan(boolean catatan) {
        this.catatan = catatan;
    }

    public boolean isCharting() {
        return charting;
    }

    public void setCharting(boolean charting) {
        this.charting = charting;
    }
    
    
}
