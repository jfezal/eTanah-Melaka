public class PermitTandatanganForm {
    String namaFizikal;
    String pathDms;
    String signPath;
    Dokumen dokumen;
    String date;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
    
    

    public String getSignPath() {
        return signPath;
    }

    public void setSignPath(String signPath) {
        this.signPath = signPath;
    }

    
    
    public String getNamaFizikal() {
        return namaFizikal;
    }

    public void setNamaFizikal(String namaFizikal) {
        this.namaFizikal = namaFizikal;
    }

    public String getPathDms() {
        return pathDms;
    }

    public void setPathDms(String pathDms) {
        this.pathDms = pathDms;
    }

    public Dokumen getDokumen() {
        return dokumen;
    }

    public void setDokumen(Dokumen dokumen) {
        this.dokumen = dokumen;
    }
    
    
}
