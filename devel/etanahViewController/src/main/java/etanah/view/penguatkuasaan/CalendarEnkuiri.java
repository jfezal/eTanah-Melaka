package etanah.view.penguatkuasaan;

import etanah.model.EnkuiriPenguatkuasaan;
import etanah.model.KodCawangan;
import java.util.Date;
import java.util.List;

/**
 *
 * @author ctzainal 
 */
public class CalendarEnkuiri {

    private String tempat;
    private String tujuan;
    private Date tarikhMula;
    private String idMohon;
    private KodCawangan cawangan;
//    private String hakmilik;
    private List<EnkuiriPenguatkuasaan> nama;

    public Date getTarikhMula() {
        return tarikhMula;
    }

    public void setTarikhMula(Date tarikhMula) {
        this.tarikhMula = tarikhMula;
    }

    public String getTujuan() {
        return tujuan;
    }

    public void setTujuan(String tujuan) {
        this.tujuan = tujuan;
    }

    public KodCawangan getCawangan() {
        return cawangan;
    }

    public void setCawangan(KodCawangan cawangan) {
        this.cawangan = cawangan;
    }

    public String getTempat() {
        return tempat;
    }

    public void setTempat(String tempat) {
        this.tempat = tempat;
    }

    public List<EnkuiriPenguatkuasaan> getNama() {
        return nama;
    }

    public void setNama(List<EnkuiriPenguatkuasaan> nama) {
        this.nama = nama;
    }

    public String getIdMohon() {
        return idMohon;
    }

    public void setIdMohon(String idMohon) {
        this.idMohon = idMohon;
    }
}
