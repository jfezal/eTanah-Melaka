package etanah.view.penguatkuasaan;
import java.io.Serializable;

/**
 *
 * @author latifah.iskak
 */
public class KehadiranEnkuiriValue implements Serializable {

    long idKehadiranEnkuiri;
    String nama;
    String noPengenalan;
    String kehadiran;
    String catatan;

    public String getCatatan() {
        return catatan;
    }

    public void setCatatan(String catatan) {
        this.catatan = catatan;
    }

    public long getIdKehadiranEnkuiri() {
        return idKehadiranEnkuiri;
    }

    public void setIdKehadiranEnkuiri(long idKehadiranEnkuiri) {
        this.idKehadiranEnkuiri = idKehadiranEnkuiri;
    }

    public String getKehadiran() {
        return kehadiran;
    }

    public void setKehadiran(String kehadiran) {
        this.kehadiran = kehadiran;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNoPengenalan() {
        return noPengenalan;
    }

    public void setNoPengenalan(String noPengenalan) {
        this.noPengenalan = noPengenalan;
    }

    

    
}
