public class Pemilik {

    private String idJenisPengenalan;
    private String noPengenalan;
    private String namaPemilik;
    private String BA;
    private String BB;
    private String jenisPB;

    public String getIdJenisPengenalan() {
        return idJenisPengenalan;
    }

    public void setIdJenisPengenalan(String idJenisPengenalan) {
        this.idJenisPengenalan = idJenisPengenalan;
    }

    public String getNamaPemilik() {
        return namaPemilik;
    }

    public void setNamaPemilik(String namaPemilik) {
        this.namaPemilik = namaPemilik;
    }

    public String getBA() {
        return BA;
    }

    public void setBA(String bA) {
        BA = bA;
    }

    public String getBB() {
        return BB;
    }

    public void setBB(String bB) {
        BB = bB;
    }

    public String getJenisPB() {
        return jenisPB;
    }

    public void setJenisPB(String jenisPB) {
        this.jenisPB = jenisPB;
    }

    public String getNoPengenalan() {
        return noPengenalan;
    }

    public void setNoPengenalan(String noPengenalan) {
        this.noPengenalan = noPengenalan;
    }
    
    
}