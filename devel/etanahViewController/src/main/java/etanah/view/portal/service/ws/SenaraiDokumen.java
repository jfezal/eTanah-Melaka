public class SenaraiDokumen {
    String namaDokumen;
    String mandatori;
    String sah;
    String muatTurun;

    public String getNamaDokumen() {
        return namaDokumen;
    }

    public void setNamaDokumen(String namaDokumen) {
        this.namaDokumen = namaDokumen;
    }

    public String getMandatori() {
        return mandatori;
    }

    public void setMandatori(String mandatori) {
        this.mandatori = mandatori;
    }

    public String getSah() {
        return sah;
    }

    public void setSah(String sah) {
        this.sah = sah;
    }

    public String getMuatTurun() {
        return muatTurun;
    }

    public void setMuatTurun(String muatTurun) {
        this.muatTurun = muatTurun;
    }
    

}
