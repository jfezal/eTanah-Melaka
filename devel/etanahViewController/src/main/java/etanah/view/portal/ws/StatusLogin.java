package etanah.view.portal.ws;

import java.util.Date;

/**
 *
 * @author faidzal
 */
public class StatusLogin {

    String idPengguna;
    String nama;
    Date tarikhterakhirlogin;
    String sts;

    public Date getTarikhterakhirlogin() {
        return tarikhterakhirlogin;
    }

    public void setTarikhterakhirlogin(Date tarikhterakhirlogin) {
        this.tarikhterakhirlogin = tarikhterakhirlogin;
    }

    public String getSts() {
        return sts;
    }

    public void setSts(String sts) {
        this.sts = sts;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getIdPengguna() {
        return idPengguna;
    }

    public void setIdPengguna(String idPengguna) {
        this.idPengguna = idPengguna;
    }

}
