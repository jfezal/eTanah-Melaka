package etanah.view.uam;

import etanah.model.Pengguna;
import java.util.Date;

/**
 *
 * @author faidzal
 */
public class ListAllPengguna {
    private Pengguna pengguna;
    private String jawatan;
    private String idKaunter;
    private String nama;
    private boolean statusOnline;
    private String terminal;
    private Date tarikhDaftar;
    private Date tarikhAkhirLogin;
    private Date tarikhAkhirAktiviti;

    public String getIdKaunter() {
        return idKaunter;
    }

    public void setIdKaunter(String idKaunter) {
        this.idKaunter = idKaunter;
    }

    public String getJawatan() {
        return jawatan;
    }

    public void setJawatan(String jawatan) {
        this.jawatan = jawatan;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public Pengguna getPengguna() {
        return pengguna;
    }

    public void setPengguna(Pengguna pengguna) {
        this.pengguna = pengguna;
    }

    public boolean isStatusOnline() {
        return statusOnline;
    }

    public void setStatusOnline(boolean statusOnline) {
        this.statusOnline = statusOnline;
    }



    public Date getTarikhDaftar() {
        return tarikhDaftar;
    }

    public void setTarikhDaftar(Date tarikhDaftar) {
        this.tarikhDaftar = tarikhDaftar;
    }

    public String getTerminal() {
        return terminal;
    }

    public void setTerminal(String terminal) {
        this.terminal = terminal;
    }

    public Date getTarikhAkhirLogin() {
        return tarikhAkhirLogin;
    }

    public void setTarikhAkhirLogin(Date tarikhAkhirLogin) {
        this.tarikhAkhirLogin = tarikhAkhirLogin;
    }

    public Date getTarikhAkhirAktiviti() {
        return tarikhAkhirAktiviti;
    }

    public void setTarikhAkhirAktiviti(Date tarikhAkhirAktiviti) {
        this.tarikhAkhirAktiviti = tarikhAkhirAktiviti;
    }
    
    
}
